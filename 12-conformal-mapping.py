# Function:
#   Build a planar conformal parameterization for closed triangle meshes.
#   For genus-zero meshes it creates a disk by cutting a short slit (or falls
#   back to a vertex-star cut if needed). For positive genus meshes it constructs
#   a short homology-based cut graph, connects it if necessary, cuts the mesh by
#   duplicating seam vertices without deleting faces, checks that the result is
#   one topological disk, and solves LSCM using EC = ED - A with two fixed
#   vertices. It also saves UV arrays and UV visualizations with wireframe,
#   Laplacian-magnitude coloring, and quadrant coloring.
#
# Difference from 02-conformal-mapping.py:
#   02-conformal-mapping.py is only a simple spectral embedding using
#   eigenvectors of the cotangent Laplacian as 2D coordinates. It does not cut
#   closed high-genus surfaces, does not guarantee a disk topology, and does not
#   solve the LSCM conformal energy. This file implements the full cut + LSCM pipeline.

