def build_cut_disk(verts, faces, genus):
    """Build a disk mesh by cutting genus-zero or positive-genus closed meshes."""
    if genus == 0:
        cut_edges, path_vertices = genus_zero_slit_cut_edges(verts, faces)
        basis = []
    else:
        path_vertices = []
        basis = shortest_homology_basis(verts, faces, genus)
        cut_edges = set()
        for cycle in basis:
            cut_edges.update(cycle["edges"])
        all_edges, _ = undirected_edges(faces)
        cut_edges = make_connected_cut_graph(verts, all_edges.keys(), cut_edges)
    cut_verts, cut_faces, new_to_old, _ = cut_mesh(verts, faces, cut_edges)
    return cut_verts, cut_faces, new_to_old, basis, path_vertices


def process_mesh(obj_path):
    """Run the full cut-and-LSCM pipeline for one OBJ file."""
    cs = ClosedSurface(str(obj_path))
    verts = cs.verts
    faces = cs.faces
    genus, chi, edge_count, boundary = mesh_genus(verts, faces)
    if boundary:
        raise RuntimeError("input mesh is not closed")

    cut_verts, cut_faces, new_to_old, basis, path_vertices = build_cut_disk(verts, faces, genus)
    cut_chi, cut_comps, loops = disk_checks(cut_verts, cut_faces)
    if cut_comps != 1 or len(loops) != 1 or cut_chi != 1:
        raise RuntimeError(
            f"cut failed: components={cut_comps}, boundary_loops={len(loops)}, chi={cut_chi}"
        )

    uv, fixed = solve_lscm(cut_verts, cut_faces, loops[0])
    if not np.all(np.isfinite(uv)):
        raise RuntimeError("LSCM produced non-finite UV coordinates")

    lap_colors = laplacian_magnitude_colors(verts, faces)
    quad_colors = quadrant_colors(verts)
    save_all_outputs(obj_path.stem, uv, cut_faces, new_to_old, lap_colors, quad_colors)

    print(f"{obj_path.name}:")
    print("  closed mesh:", len(verts), edge_count, len(faces), "chi", chi, "genus", genus)
    print("  cut mesh:", len(cut_verts), len(undirected_edges(cut_faces)[0]), len(cut_faces))
    print("  disk checks:", "components", cut_comps, "boundary_loops", len(loops), "chi", cut_chi)
    print("  fixed vertices:", fixed)
    if path_vertices:
        print("  genus-zero slit vertices:", len(path_vertices), "from", path_vertices[0], "to", path_vertices[-1])
    if basis:
        print("  homology lengths:", [round(x["length"], 6) for x in basis])
    print("  saved prefix:", str(OUTPUT_DIR / obj_path.stem))


def main():
    """Run the pipeline on closed genus-zero meshes and the genus-two example."""
    OUTPUT_DIR.mkdir(exist_ok=True)
    for obj_path in sorted(INPUT_DIR.glob("*.obj")):
        try:
            cs = ClosedSurface(str(obj_path))
            genus, chi, _, boundary = mesh_genus(cs.verts, cs.faces)
            if boundary or genus not in DEFAULT_GENERA:
                reason = "open mesh" if boundary else f"genus {genus}"
                print(f"{obj_path.name}: skipped by default ({reason})")
                continue
            process_mesh(obj_path)
        except Exception as exc:
            print(f"{obj_path.name}: skipped ({exc})")


if __name__ == "__main__":
    main()
